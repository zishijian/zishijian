// 切换语言
function switchLang(lang) {
  // 验证语言参数，确保只接受有效的语言代码
  if (!['zh', 'zh-TW', 'en'].includes(lang)) {
    lang = 'zh';
  }
  if (lang === currentLang) return;
  currentLang = lang;
  localStorage.setItem('zishijian-lang', lang);
  applyLang();
}

// 创建语言切换按钮
function createLangSwitcher() {
  if (document.getElementById('lang-switcher')) return;
  
  const isHomePage = window.location.pathname.endsWith('index.html') || 
                     window.location.pathname.endsWith('/') || 
                     window.location.pathname.endsWith('/zishijian/') ||
                     window.location.pathname.endsWith('/zishijian/index.html');
  
  const switcher = document.createElement('div');
  switcher.id = 'lang-switcher';
  switcher.style.cssText = `
    position: fixed;
    bottom: 10px;
    right: 10px;
    z-index: 9999;
    background: rgba(10, 9, 8, 0.75);
    border: 1px solid rgba(201, 162, 39, 0.25);
    border-radius: 14px;
    padding: 3px 8px;
    display: flex;
    gap: 2px;
    backdrop-filter: blur(12px);
    box-shadow: 0 4px 25px rgba(0, 0, 0, 0.6), 0 0 20px rgba(201, 162, 39, 0.1);
  `;
  
  const langOptions = [
    { code: 'zh', text: '简' },
    { code: 'zh-TW', text: '繁' },
    { code: 'en', text: 'EN' }
  ];
  
  langOptions.forEach(opt => {
    const btn = document.createElement('button');
    btn.className = 'lang-btn';
    btn.dataset.lang = opt.code;
    btn.textContent = opt.text;
    btn.style.cssText = `
      background: transparent;
      border: 1px solid transparent;
      color: ${opt.code === currentLang ? '#c9a227' : '#9a958a'};
      padding: 3px 8px;
      border-radius: 14px;
      cursor: pointer;
      font-size: 11px; font-family: 'Inter', sans-serif; letter-spacing: 0.3px;
      font-weight: ${opt.code === currentLang ? '600' : '400'};
      transition: all 0.3s ease;
      opacity: ${opt.code === currentLang ? '1' : '0.7'};
    `;
    
    btn.addEventListener('click', () => switchLang(opt.code));
    btn.addEventListener('mouseenter', () => {
      if (btn.dataset.lang !== currentLang) {
        btn.style.opacity = '1';
        btn.style.color = '#d4a84b';
      }
    });
    btn.addEventListener('mouseleave', () => {
      if (btn.dataset.lang !== currentLang) {
        btn.style.opacity = '0.7';
        btn.style.color = '#9a958a';
      }
    });
    
    switcher.appendChild(btn);
  });
  
  // 所有页面统一右下角
  
  document.body.appendChild(switcher);
  
  if (window.innerWidth < 768) {
    switcher.style.bottom = '6px';
    switcher.style.right = '6px';
    switcher.style.padding = '4px 8px';
    switcher.querySelectorAll('.lang-btn').forEach(btn => {
      btn.style.padding = '4px 10px';
      btn.style.fontSize = '11px';
    });
  }
}

